    private void showItemBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showItemBtnActionPerformed
        try {
            ArrayList<ItemModel> itemList = ItemController.getAllItem();
            DefaultTableModel dtm = (DefaultTableModel) ItemTable.getModel();
            dtm.setRowCount(0);
            for (ItemModel item : itemList) {
                Object[] rowData={item.getItemCode(),item.getDescription(),item.getPackSize(),item.getUnitPrice(),item.getQtyOnHand()};
                dtm.addRow(rowData);
            }

        } catch (SQLException ex) {
            Logger.getLogger(ViewItemForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Driver not found...");
        }
    }//GEN-LAST:event_showItemBtnActionPerformed

