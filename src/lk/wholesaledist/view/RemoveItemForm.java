    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        try {
            ItemModel item = ItemController.searchItemCode(itemCodeTxt.getText());
            if(item != null){
                descriptionTxt.setText(item.getDescription());
                packSizeTxt.setText(item.getPackSize());
                unitPriceTxt.setText(Double. toString(item.getUnitPrice()));
                qtyOnHandTxt.setText(Integer.toString(item.getQtyOnHand()));
                
            }else{
                JOptionPane.showMessageDialog(this, "Item not found...");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Driver not found...");
        }
    }//GEN-LAST:event_searchBtnActionPerformed

